$(function () {
    $('div#headerNOANSI').hide();
    $('button#removeANSI').click(function () {
        $('div#headerNOANSI').show();
        $('div#headerANSI').hide();
        $('.sbk').addClass('nosbk').removeClass('sbk');
        $('html').addClass('reset');
        $('span').addClass('reset');
        $('pre').addClass('reset');
        $('div').addClass('reset')
    });
    $('button#ANSIcolor').click(function () {
        $('div#headerNOANSI').hide();
        $('div#headerANSI').show();
        $('.nosbk').addClass('sbk').removeClass('nosbk');
        $('html').removeClass('reset');
        $('span').removeClass('reset');
        $('pre').removeClass('reset');
        $('div').removeClass('reset')
    })
});
